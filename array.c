#include <stdio.h>

int main() {
    int myNumbers[] = {25, 50, 75, 100};

    // Accessing the first element (index 0)
    printf("%d\n", myNumbers[0]); // Output: 25

    // Changing the value of the third element (index 2)
    myNumbers[2] = 90;
    printf("%d\n", myNumbers[2]); // Output: 90

    printf("Hello");
    
    return 0;
}
